<?php


class Calculator
{

    public function add($a, $b) : int {
        return $a + $b;
    }

    public function substract($a, $b) : int {
        return $a - $b;
    }

    public function divide($a, $b) : int {
        return $a / $b;
    }

    public function multiply($a, $b) : int {
        return $a * $b;
    }
}
